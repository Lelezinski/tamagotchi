/**
 * @brief Converts Integer to String
 * @param string
 * @param number
 */
int toString(uint8_t *str, int num)
{
	int i, rem, len = 0, n;

	// Handling the case 0
	if (num == 0)
	{
		str[0] = '0';
		str[1] = '\0';
		return 1;
	}
	n = num;
	while (n != 0)
	{
		len++;
		n /= 10;
	}
	for (i = 0; i < len; i++)
	{
		rem = num % 10;
		num = num / 10;
		str[len - (i + 1)] = rem + '0';
	}
	str[len] = '\0';
	return len - 1;
}

void toHHMMSS(uint8_t *buffer, uint16_t hrs, uint8_t min, uint8_t sec)
{
	uint8_t temp[2];
	strcpy((char *)buffer, "Age ");

	// Hours
	if (hrs > 99)
	{
		// To not overflow strcat
		hrs = 99;
	}

	toString(temp, hrs);
	if (hrs < 10)
	{
		strcat((char *)buffer, "0");
	}
	strcat((char *)buffer, (char *)temp);

	// Mins
	strcat((char *)buffer, ":");
	toString(temp, min);
	if (min < 10)
	{
		strcat((char *)buffer, "0");
	}
	strcat((char *)buffer, (char *)temp);

	// Secs
	strcat((char *)buffer, ":");
	toString(temp, sec);
	if (sec < 10)
	{
		strcat((char *)buffer, "0");
	}
	strcat((char *)buffer, (char *)temp);
}

/**
 * @brief Reproduces the given note on the Loudspeaker output using MR0 TIM2 to generate the wanted frequence
 * @param note
 */
void playNote(uint32_t note)
{
	disable_timer(2);
	reset_timer(2);
	if (note > 200) 
	{
		// To lower bound is used to implement a pause 
		// and to avoid slowing down the system too much
		LPC_TIM2->MR0 = note;
		enable_timer(2);
	}
}

/**
 * @brief Plays the note array on the Loudspeaker using TIM3 and TIM2
 * @param notes
 */
void playSong(uint32_t * notes, uint32_t delay_ms)
{
	disable_timer(3);
	LPC_TIM3->MR0 = delay_ms;
	game.currentSong.notesArray = notes;
	game.currentSong.currentNote = 0;
	reset_timer(3);
	enable_timer(3);
}

